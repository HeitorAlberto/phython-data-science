A = {1, 2, 3}
B = {4, 3, 2}

print(B)  #Retorna "2, 3, 4"       #Esse tipo de estrutura coloca os itens em ordem automaticamente




#Esse tipo de estrutura tem vários métodos parecidos com os conjuntos matemáticos
A.union(B)             #{1, 2, 3, 4} - todos os elementos de ambos os conjuntos
A.intersection(B)      #{2, 3} = apenas elementos comuns aos dois conjuntos
